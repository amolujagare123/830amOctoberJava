package InheritancePolymorphism;

public class CastingDemo {

    public static void main(String[] args) {
        int a =10;
        int b =3;

        double c = (double) a/b;
// casting - if you want to store one type of data into another type
// we use casting , Here the value to be stored into anoter type has to have
// datatype/class type written in round brackets infront of it
        System.out.println(c);
    }
}
